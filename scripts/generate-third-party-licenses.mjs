import fs from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const repositoryRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..")
const outputPath = path.join(repositoryRoot, "THIRD_PARTY_LICENSES.txt")
const checkOnly = process.argv.includes("--check")

// These packages contribute code, styles, icons, source-owned component
// templates, or font files to StoreShot's distributable output.
const distributedPackages = [
  "@base-ui/react",
  "@fontsource-variable/geist",
  "class-variance-authority",
  "clsx",
  "commander",
  "fabric",
  "lucide-react",
  "open",
  "react",
  "react-dom",
  "react-resizable-panels",
  "shadcn",
  "tailwind-merge",
  "tailwindcss",
  "tw-animate-css",
]

// Any newly introduced license expression must be reviewed before it is added
// here. This is a guardrail, not a legal compatibility determination.
const reviewedLicenseExpressions = new Set([
  "(BSD-2-Clause OR MIT OR Apache-2.0)",
  "(MIT OR WTFPL)",
  "0BSD",
  "Apache-2.0",
  "BlueOak-1.0.0",
  "BSD-2-Clause",
  "BSD-3-Clause",
  "CC-BY-4.0",
  "ISC",
  "MIT",
  "MIT-0",
  "MPL-2.0",
  "OFL-1.1",
  "Python-2.0",
])

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"))
}

function packageDirectory(packageName) {
  return path.join(repositoryRoot, "node_modules", ...packageName.split("/"))
}

function licenseFiles(packageName) {
  const directory = packageDirectory(packageName)
  if (!fs.existsSync(directory)) {
    throw new Error(`${packageName} is not installed; run npm ci first`)
  }

  return fs.readdirSync(directory)
    .filter((fileName) => /^(licen[cs]e)(\..+)?$/i.test(fileName))
    .sort()
    .map((fileName) => path.join(directory, fileName))
}

function validateDependencyLicenses() {
  const packageJson = readJson(path.join(repositoryRoot, "package.json"))
  const directPackages = Object.keys({
    ...packageJson.dependencies,
    ...packageJson.devDependencies,
  })

  const missingDirectLicense = directPackages.filter((packageName) => licenseFiles(packageName).length === 0)
  if (missingDirectLicense.length > 0) {
    throw new Error(`Direct packages without a license file: ${missingDirectLicense.join(", ")}`)
  }

  const packageLock = readJson(path.join(repositoryRoot, "package-lock.json"))
  const unreviewed = new Map()

  for (const [packagePath, metadata] of Object.entries(packageLock.packages)) {
    if (packagePath === "") continue
    const expression = typeof metadata.license === "string" ? metadata.license : "MISSING"
    if (!reviewedLicenseExpressions.has(expression)) {
      const entries = unreviewed.get(expression) ?? []
      entries.push(packagePath)
      unreviewed.set(expression, entries)
    }
  }

  if (unreviewed.size > 0) {
    const details = [...unreviewed.entries()]
      .map(([expression, packagePaths]) => `${expression}: ${packagePaths.join(", ")}`)
      .join("\n")
    throw new Error(`Missing or unreviewed dependency licenses:\n${details}`)
  }

  const requiredAssetLicenses = [
    "src/ui/public/artwork/tabler/LICENSE",
    "src/ui/public/licenses/geist-OFL-1.1.txt",
    "src/ui/public/mockup-bundles/frameup-free/LICENSE",
  ]
  const missingAssetLicenses = requiredAssetLicenses.filter(
    (filePath) => !fs.existsSync(path.join(repositoryRoot, filePath)),
  )
  if (missingAssetLicenses.length > 0) {
    throw new Error(`Missing redistributed asset licenses: ${missingAssetLicenses.join(", ")}`)
  }
}

function generateLicenseText() {
  const sections = [
    "STORESHOT THIRD-PARTY PACKAGE LICENSES",
    "======================================",
    "",
    "Generated by `npm run licenses` from the exact packages installed by package-lock.json.",
    "These packages contribute code, styles, icons, templates, or fonts to StoreShot's",
    "distributable output. Copyright notices and license terms below are reproduced",
    "without modification from the installed packages.",
  ]

  for (const packageName of distributedPackages) {
    const metadata = readJson(path.join(packageDirectory(packageName), "package.json"))
    const files = licenseFiles(packageName)
    if (files.length === 0) throw new Error(`${packageName} has no license file`)

    sections.push(
      "",
      "",
      "--------------------------------------------------------------------------------",
      `${packageName} ${metadata.version}`,
      `Declared license: ${metadata.license ?? "not declared"}`,
      "--------------------------------------------------------------------------------",
    )

    for (const filePath of files) {
      sections.push("", fs.readFileSync(filePath, "utf8").trim())
    }
  }

  return `${sections.join("\n")}\n`
}

validateDependencyLicenses()
const generated = generateLicenseText()

if (checkOnly) {
  const existing = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, "utf8") : ""
  if (existing !== generated) {
    throw new Error("THIRD_PARTY_LICENSES.txt is stale; run npm run licenses")
  }
  console.log("Dependency and redistributed asset licenses are accounted for.")
} else {
  fs.writeFileSync(outputPath, generated)
  console.log(`Wrote ${path.relative(repositoryRoot, outputPath)}`)
}
